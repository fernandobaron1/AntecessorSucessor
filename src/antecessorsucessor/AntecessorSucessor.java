
package antecessorsucessor;


public class AntecessorSucessor {

    
    public static void main(String[] args) {
        
        int numero = 2;
        int sucessor = 0;
        int antecessor = 0;
        
        antecessor = numero - 1;
        sucessor = numero + 1;
        
        System.out.println("Número Antecessor: "+antecessor);
        System.out.println("Número Sucessor: "+sucessor);
       
    }
    
}
